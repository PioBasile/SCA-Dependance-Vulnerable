import optuna

def objective(trial):
    # Hyperparameters
    lr = trial.suggest_float("lr", 1e-5, 5e-5, log = True)
    dropout_prob = trial.suggest_float("dropout", 0.1, 0.4)
    wd = trial.suggest_float("weight_decay", 0.01, 0.1)

    # 20K random lines
    try:
        df = pd.read_csv(
            "dataset_cvss.csv",
            engine='python',
            on_bad_lines='warn',
            quoting=0
        )
    except Exception as e:
        print(f"Error importing dataset : {e}")

    df_optuna = df.sample(n = 20000, random_state = 42)
    df_train_opt, df_val_opt = train_test_split(
        df_optuna, test_size = 0.2, random_state = 42, stratify = df_optuna['attackVector']
    )

    t_loader = DataLoader(CVEDataset(df_train_opt), batch_size = BATCH_SIZE, shuffle = True)
    v_loader = DataLoader(CVEDataset(df_val_opt), batch_size = BATCH_SIZE)

    # Config
    model = CVSSModel(dropout_rate = dropout_prob).to(DEVICE)
    optimizer = torch.optim.AdamW(model.parameters(), lr = lr, weight_decay = wd)
    criterion = nn.CrossEntropyLoss()

    # Only 2 epochs
    for epoch in range(2):
        # Train
        model.train()
        for batch in t_loader:
            ids = batch["input_ids"].to(DEVICE)
            mask = batch["attention_mask"].to(DEVICE)
            lbls = batch["labels"].to(DEVICE)

            logits = model(ids, mask)
            loss = sum(criterion(logits[i], lbls[:, i]) for i in range(8))

            optimizer.zero_grad()
            loss.backward()
            optimizer.step()

        # Validation
        model.eval()
        val_f1_scores = []
        with torch.no_grad():
            all_preds = [[] for _ in range(8)]
            all_labels = [[] for _ in range(8)]

            for batch in v_loader:
                ids, mask, lbls = batch["input_ids"].to(DEVICE), batch["attention_mask"].to(DEVICE), batch["labels"].to(DEVICE)
                logits = model(ids, mask)
                for i in range(8):
                    all_preds[i].extend(logits[i].argmax(1).cpu().numpy())
                    all_labels[i].extend(lbls[:, i].cpu().numpy())

            for i in range(8):
                val_f1_scores.append(f1_score(all_labels[i], all_preds[i], average="weighted"))

        current_mean_f1 = np.mean(val_f1_scores)

        # Print results
        trial.report(current_mean_f1, epoch)
        if trial.should_prune():
            raise optuna.exceptions.TrialPruned()

    return current_mean_f1

# Study
study = optuna.create_study(direction = "maximize")
study.optimize(objective, n_trials = 12)

print("\n--- OPTUNA RESULTS ---")
print(f"Best Mean F1 : {study.best_value:.4f}")
print(f"Best hyperparameters : {study.best_params}")

best_lr = study.best_params['lr']
best_dropout = study.best_params['dropout']
best_wd = study.best_params['weight_decay']