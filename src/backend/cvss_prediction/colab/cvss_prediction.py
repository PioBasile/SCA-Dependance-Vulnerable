df_train = df.sample(n = 187_034, random_state = 42)

# Config
df_train_full, df_val = train_test_split(df_train, test_size = 0.2, random_state = 42, stratify = df_train['attackVector'])

train_loader = DataLoader(CVEDataset(df_train_full), batch_size=BATCH_SIZE, shuffle=True)
val_loader   = DataLoader(CVEDataset(df_val),        batch_size=BATCH_SIZE)

model = CVSSModel().to(DEVICE)
# optimizer = torch.optim.AdamW(model.parameters(), lr = LR)
optimizer = torch.optim.AdamW(model.parameters(), lr = LR, weight_decay = WD) # Optuna
best_f1 = 0.0

SAVE_PATH = "/content/drive/MyDrive/cvss_model"
cpt = 0

for epoch in range(EPOCHS):
    # Train
    model.train()
    train_preds_all, train_labels_all = [[] for _ in range(8)], [[] for _ in range(8)]
    train_loss = 0

    for batch in tqdm(train_loader, desc=f"Epoch {epoch+1} [Train]"):
        ids, mask, lbls = batch["input_ids"].to(DEVICE), batch["attention_mask"].to(DEVICE), batch["labels"].to(DEVICE)

        logits = model(ids, mask)
        loss = sum(criterion(logits[i], lbls[:, i]) for i in range(8))

        optimizer.zero_grad()
        loss.backward()
        optimizer.step()
        train_loss += loss.item()

        for i in range(8):
            train_preds_all[i].extend(logits[i].argmax(1).cpu().numpy())
            train_labels_all[i].extend(lbls[:, i].cpu().numpy())

    # Validation
    model.eval()
    val_preds_all, val_labels_all = [[] for _ in range(8)], [[] for _ in range(8)]
    val_loss = 0

    with torch.no_grad():
        for batch in tqdm(val_loader, desc=f"Epoch {epoch+1} [Val]"):
            ids, mask, lbls = batch["input_ids"].to(DEVICE), batch["attention_mask"].to(DEVICE), batch["labels"].to(DEVICE)
            logits = model(ids, mask)

            loss = sum(criterion(logits[i], lbls[:, i]) for i in range(8))
            val_loss += loss.item()

            for i in range(8):
                val_preds_all[i].extend(logits[i].argmax(1).cpu().numpy())
                val_labels_all[i].extend(lbls[:, i].cpu().numpy())

    # Print results
    print(f"\n--- Epoch {epoch + 1} ---")

    train_current_f1_scores = []
    train_current_mean_scores = []

    val_current_f1_scores = []
    val_current_mean_scores = []

    for i, name in enumerate(METRICS):
        train_acc = accuracy_score(train_labels_all[i], train_preds_all[i])
        train_f1  = f1_score(train_labels_all[i], train_preds_all[i], average="weighted")

        val_acc = accuracy_score(val_labels_all[i], val_preds_all[i])
        val_f1  = f1_score(val_labels_all[i], val_preds_all[i], average="weighted")

        train_current_mean_scores.append(train_acc)
        train_current_f1_scores.append(train_f1)
        val_current_mean_scores.append(val_acc)
        val_current_f1_scores.append(val_f1)

        print(f"\n\t{name:<25}\n\tTraining > Accuracy : {train_acc:.3f} | F1 : {train_f1:.3f}\n\tValidation > Accuracy : {val_acc:.3f} | F1 : {val_f1:.3f}")

    train_mean = np.mean(train_current_mean_scores)
    val_mean = np.mean(val_current_mean_scores)
    train_mean_f1 = sum(train_current_f1_scores) / 8
    val_mean_f1 = sum(val_current_f1_scores) / 8

    print(f"\n\n  >> Training : Mean accuracy : {train_mean:.4f} | Mean F1 : {train_mean_f1:.4f} | Train Loss: {train_loss/len(train_loader):.4f}")
    print(f"  >> Validation : Mean accuracy : {val_mean:.4f} | Mean F1 : {val_mean_f1:.4f} | Val Loss: {val_loss/len(val_loader):.4f}")

    # Save
    cpt += 1
    if val_mean_f1 > best_f1:
        best_f1 = val_mean_f1
        print(f"\nNew best model : {best_f1:.4f} F1")

    REAL_SAVE_PATH = SAVE_PATH + f"_epoch_{cpt}.pt"
    torch.save(model.state_dict(), REAL_SAVE_PATH)
    print(f"\nModel saved in : {REAL_SAVE_PATH}")

